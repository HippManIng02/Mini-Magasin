    /**
     * Cette fonction permet de calculer le total
     * @return 
     */
    private double calculTotal(){
        double resultat = 0;
        ProduitPointVente p;
        for (Object[] object : listeObject) {
            p = pVenteControlleur.getProduitPointVenteById(listeppVente, Integer.parseInt(object[0].toString()));
           resultat += venteControlleur.calculSousTotal(Double.parseDouble(object[3].toString().trim()),Integer.parseInt(object[2].toString().trim()), p.getProduit().getTva());
        }
        return resultat;
    }
    
