    private void tResultatRechercheMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tResultatRechercheMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2 ){
            DefaultTableModel model = (DefaultTableModel) tResultatRecherche.getModel();
            JTable target = (JTable)evt.getSource();
            Object[] rowData = new Object[model.getColumnCount()];
            for (int i = 0; i < model.getColumnCount(); i++) {
               rowData[i] = model.getValueAt(target.getSelectedRow(), i);
            }
            int quantite = Integer.parseInt(JOptionPane.showInputDialog(rootPane,"Quantité Comande"));
            if(quantite > Integer.parseInt(rowData[2].toString())){
                JOptionPane.showMessageDialog(rootPane, "La quantité choisie est supérieur au stock; veuillez recommencer!", "Warning", JOptionPane.WARNING_MESSAGE);
            }else{
                rowData[2] = quantite;
                listeObject.add(rowData);
                initTableTproduitsVente();
            }
            
        }
    }//GEN-LAST:event_tResultatRechercheMouseClicked

